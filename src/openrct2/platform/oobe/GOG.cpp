    static bool extractFiles(u8string_view binFile, std::span<const InnoArchiveFile> files, u8string_view targetFolder)
    {
        auto fs = FileStream(binFile, FileMode::open);
        char header[8]{};
        fs.Read(header, 7);
        if (strncmp(header, "idska32", 7) != 0)
            return false;

        Path::DeleteDirectory(targetFolder);
        Path::CreateDirectory(targetFolder);

        for (const auto& file : files)
        {
            auto outputPath = Path::Combine(targetFolder, file.filename);
            auto parentFolder = Path::GetDirectory(outputPath);
            Path::CreateDirectory(parentFolder);

            fs.SetPosition(file.offset);
            auto outputFs = FileStream(outputPath, FileMode::append);
            if (!Compression::zlibDecompress(
                    fs, file.compressedSize, outputFs, file.unCompressedSize, Compression::ZlibHeaderType::zlib))
            {
                auto error = "Cannot extract " + u8string(file.filename) + "!";
                LOG_ERROR(error.c_str());
            }
        }

        return true;
    }

    bool isRCT2BinFile(u8string_view binFile)
    {
        return File::GetSize(binFile) == 580528159;
    }

    bool isRCT1BinFile(u8string_view binFile)
    {
        return File::GetSize(binFile) == 411784351;
    }

    bool extractRCT2Files(u8string_view binFile, u8string_view targetFolder)
    {
        if (!isRCT2BinFile(binFile))
            return false;

        return extractFiles(binFile, kRCT2Files, targetFolder);
    }

    bool extractRCT1Files(u8string_view binFile, u8string_view targetFolder)
    {
        if (!isRCT1BinFile(binFile))
            return false;

        return extractFiles(binFile, kRCT1Files, targetFolder);
    }
